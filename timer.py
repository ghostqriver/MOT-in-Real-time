import time


class Timer(object):
    """A simple timer."""
    def __init__(self):
        self.total_time = 0.
        self.calls = 0
        self.start_time = 0.
        self.diff = 0.
        self.average_time = 0.

        self.duration = 0.

    def tic(self):
        # when call tic: update start_time to this moment 
        # while start the prediction.inference
        self.start_time = time.time()

    def toc(self, average=True):
        # when call toc: 
        # diff = time from start pred to finish track
        # total_time = summation all diff
        # call = how many times it is called
        # average = averaged diff
        # while finish the tracking
        self.diff = time.time() - self.start_time
        self.total_time += self.diff
        self.calls += 1
        self.average_time = self.total_time / self.calls
        if average:
            self.duration = self.average_time
        else:
            self.duration = self.diff
        return self.duration

    def clear(self):
        self.total_time = 0.
        self.calls = 0
        self.start_time = 0.
        self.diff = 0.
        self.average_time = 0.
        self.duration = 0.